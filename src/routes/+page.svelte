    <h2>Player Characters</h2>
        <section>
            <p>
            You take on the role of a <b>player character (PC)</b>. 
            PCs are heroic (or perhaps antiheroic) adventurers
            who brave perilous dangers seeking fame and fortune. 
            Throughout this document, the word <i>you</i> will
            refer primarily to the character you are controlling.
            </p>

            <p>
            The following are some of the most important traits that define a PC:
            </p>

            <ul>
                <li><b>Race</b> — their biological or magical species.</li>
                <li><b>Class</b> — their archetypal role, which will determine most of their gameplay.</li>
                <li><b>Level</b> — a number representing how powerful and experienced they are.</li>
                <li><b>Attributes</b> — numbers that quantify their strengths and weaknesses.</li>
                <li>Their appearance, personality, motivations, flaws, and other traits as a character within a story.</li>
            </ul>

        </section>

