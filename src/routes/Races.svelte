    <h1>Chapter 2: Races</h1>

    <p>
        This game takes place in a fantasy world where many different intelligent species,
        called <b>races</b>, coexist. Your race determines these aspects of your character:
    </p>

    <ul>
        <li>
            <b>Anatomy and appearance.</b>
            Your choice of race provides general guidelines
            for your appearance such as your proportions;
            the possible range of colors for things like eyes, skin, fur, or scales;
            whether you have claws, fangs, a tail, or other unusual appendages;
            and any other unique characteristics you might have.
            These descriptions are by no means exhaustive, and you are free to deviate
            from them (within reason, e.g. don't give yourself wings).
        </li>
        <li>
            <b>Size.</b>
            Size is a keyword that categorizes how much space a creature takes up.
            Most playable races are <b>medium</b>, which is around
            5 to 7 feet tall (approximately the size of an adult human).
            Some races are <b>small</b> (about half as large as a medium creature),
            and some are <b>large</b> (about twice as large).
            Your size is used to determine things like 
            where you're able to fit and whether or not you can grapple someone.
            Some races allow you to choose from two sizes when creating your character.
        </li>
        <li>
            <b>Creature types.</b>
            Creatures are categorized into types
            based on their general anatomy and supernatural origin.
            With few exceptions, playable races are <b>humanoid</b> â€”
            relatively mundane beings that resemble humans in sapience
            and anatomy (one head, two arms and legs, etc)
            Some races have more than one type, such as <i>fiend humanoid</i>,
            to represent their otherworldly ties.
            Creature types don't have any inherent rules,
            but some abilities only work on certain kinds of creatures.
        </li>
        <li>
            <b>Racial features.</b>
            Your race grants you a set of <b>core features</b> representing
            special abilities directly tied to your particular physiology
            or inherent supernatural powers. You will also get a set of <b>major features</b>
            that you can choose from at certain levels, representing latent abilities
            that awaken with maturity or experience.
        </li>
    </ul>

