        <h2>Dragonborn</h2>

        <p>
            <i>Medium or Large dragon humanoid</i>
            <br />
            A dragonborn is the distant descendant of a union between a dragon and a humanoid.
            They share many traits in common with their draconic ancestors: scaly hides; serpentine faces;
            sharp teeth and claws; a tail; and sometimes even small vestigial wings.
        </p>

        <aside>
            <h3>Dragon Lineages</h3>
            <p>
                There are six main lineages of dragons, distinguished by color.
                A dragon's color determines its preferred habitat,
                what it tends to hoard, and most importantly: the type of breath it expels.
                When you create your dragonborn, choose one of these lineages.
                This choice will be used to determine the damage type associated with some of your features,
                and can also help guide your characterization.
            </p>

            <ul>
                <li>
                    <b>Black.</b>
                    Black dragons have a <b>necrotic</b> breath that withers the living and raises the dead.
                    They value hoards of knowledge and secrets over material things,
                    though ancient artifacts and rare tomes do greatly entice them.
                </li>
                <li>
                    <b>Blue.</b>
                    Blue dragons belch <b>shocking</b> electricity and their wingbeats spur thunderstorms.
                    They make their lairs near the sea so they may hoard its treasures:
                    coral, shells, pearls, and the cargo of ships.
                </li>
                <li>
                    <b>Gold.</b>
                    Gold dragons fire beams of <b>radiant</b> sunlight as their breath.
                    They favor deserts and similarly hot and barren landscapes.
                    They hoard jewels, glassware, and whatever else glitters brilliantly in the light.
                </li>
                <li>
                    <b>Green.</b>
                    Green dragons exhale a miasma of <b>toxic</b> gas.
                    They dwell in swamps and deep forests, where they tend
                    to gardens of rare flora and fauna as their hoards.
                </li>
                <li>
                    <b>Red.</b>
                    Red dragons iconically roast their foes with a breath of <b>burning</b> flames.
                    They inhabit the stony crags of canyons and mountains,
                    sleeping atop massive piles of gold, silver, and other precious metals.
                </li>
                <li>
                    <b>White.</b>
                    White dragons posses a <b>freezing</b> breath, and likewise inhabit frigid climates.
                    They hoard metalwork, usually preferring steel and iron to gold and silver.
                </li>
            </ul>
        </aside>

        <h3>Dragonborn Core Features</h3>

        <article>
            <h4>Breath Weapon</h4>
            <p>
                As an action, you can spend 2 Resource to perform a draconic breath attack,
                which is a melee energy attack against up to 3 targets next to you. 
                The targeted defense is FORT for Black, Green, and White lineages,
                and REF for Blue, Gold, and Red.
                The attack deals 2/4/6 damage of the type associated with your lineage.
            </p>
        </article>


        <article>
            <h4>Chromatic Scales</h4>
            <ul>
                <li>You have resistance to the damage type associated with your lineage.</li>
                <li>You can use your FORT instead of REF to determine your AC.</li>
            </ul>
        </article>

