        <h2>Dwarf</h2>

        <p>
            <i>Medium humanoid</i>
            <br />
            Dwarves are folk born from deep within the earth.
            They resemble humans in appearance, but are on average much shorter and stouter.
            Their skin takes tones of earth and stone, from muddy brown to slate gray.
            All dwarves grow thick facial hair from a young age,
            usually having long luscious beards by adulthood.
        </p>

        <h3>Dwarf Core Features</h3>

        <article>
            <h4>Stoneborne Resilience</h4>
            <p>
                You have resistance to toxic damage and are immune to the effects of any ingested poison.
            </p>
        </article>

        <article>
            <h4>Tremorsense</h4>
            <p>
                As long as you're standing on a solid surface,
                you can perceive your immediate surroundings (melee distance)
                through vibrations along the ground.
            </p>
        </article>

